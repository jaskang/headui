<script setup lang="ts">
import { computed, type PropType } from 'vue'
import { Listbox, ListboxButton, ListboxOption, ListboxOptions } from '@headlessui/vue'
import CheckIcon from '../Icon/CheckIcon.vue'
import ChevronDownIcon from '../Icon/ChevronDownIcon.vue'
import { useModelValue } from '../use/useModelValue'
import type { SelectOption } from './types'

defineOptions({ name: 'TSelect', inheritAttrs: false })

const emit = defineEmits(['update:value', 'change', 'select'])
defineSlots<{ label?: (props: { selected: boolean; item?: SelectOption; placeholder?: string }) => any }>()
const props = defineProps({
  value: [String, Number],
  options: {
    type: Array as PropType<SelectOption[]>,
    default: () => [],
  },
  size: String as PropType<'sm' | 'md' | 'lg'>,
  disabled: Boolean,
  clearable: Boolean,
  placeholder: String,
})

const [modelValue, setModelValue] = useModelValue(props, {
  onChange: (v: string) => {
    emit('change', v)
  },
})

const currItem = computed(() => props.options.find(item => item.value === modelValue.value))

const label = computed(() => currItem.value?.label || '')
</script>
<template>
  <Listbox :model-value="modelValue" @update:model-value="setModelValue">
    <div class="relative" v-bind="$attrs">
      <ListboxButton
        class="focus:border-primary focus:ring-primary-500 flex h-9 w-full cursor-pointer items-center gap-1 rounded-md border border-gray-200 bg-white px-1 text-left text-sm shadow-sm outline-none focus:z-10 focus:ring-1 focus:outline-none disabled:cursor-not-allowed disabled:opacity-50"
        :disabled
      >
        <div class="flex flex-1 items-center overflow-hidden pl-2 text-nowrap">
          <slot v-if="label" name="label" :selected="false" :item="currItem" :placeholder>
            <span class="w-full text-nowrap text-ellipsis">
              {{ label }}
            </span>
          </slot>
          <span v-else class="text-gray-400">
            {{ placeholder }}
          </span>
        </div>
        <ChevronDownIcon class="mr-1 h-4 w-4 shrink-0"></ChevronDownIcon>
      </ListboxButton>
      <transition
        leave-active-class="transition duration-100 ease-in"
        leave-from-class="opacity-100"
        leave-to-class="opacity-0"
      >
        <ListboxOptions
          class="bg-background absolute z-[999] mt-1 max-h-60 w-full space-y-1 overflow-auto rounded-md p-1 text-sm shadow-lg ring-1 ring-gray-200 focus:outline-none"
        >
          <ListboxOption
            v-for="item in options"
            :key="item.value"
            :value="item.value"
            :disabled="item.disabled"
            as="template"
            v-slot="{ active, selected, disabled }"
          >
            <div
              class="relative flex min-h-9 w-full cursor-pointer items-center gap-1 overflow-hidden rounded transition-colors"
              :class="[disabled ? 'pointer-events-none opacity-50' : '', active ? 'bg-input-background-hover' : '']"
            >
              <div class="flex flex-1 items-center overflow-hidden pl-2 text-nowrap">
                <slot name="label" :selected="selected" :item="item">
                  <span class="w-full text-nowrap text-ellipsis" :class="[selected ? 'font-medium' : '']">
                    {{ item.label }}
                  </span>
                </slot>
              </div>
              <CheckIcon v-if="selected" class="mr-2 h-4 w-4 shrink-0" />
            </div>
          </ListboxOption>
        </ListboxOptions>
      </transition>
    </div>
  </Listbox>
